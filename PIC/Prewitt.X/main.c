void operadorPrewitt(void)
{
    // MASCARAS DE PREWITT
    // int mascara[3][3] = {{1, 0, -1},{1, 0, -1},{1, 0, -1}};
    int mascara[3] = {1, 0, -1};
    int resultado_mascara_x = 0, resultado_mascara_y = 0;//, resultado_final = 0;
    unsigned char linha, coluna;
    // Matriz 3x3 que pegar� os pixels ao redor do qual estamos operando
    unsigned char matriz[3][3];
    
    for(linha = 1; linha < LINHAS-1; linha++)
    {
        for(coluna = 1; coluna < COLUNAS-1; coluna++)
        {
            /*
             * Matriz 3x3 que pegar� os pixels ao redor do qual estamos operando
             * |---|---|---|
             * | 1 | 2 | 3 |
             * |---|---|---|
             * | 4 | 5 | 6 |
             * |---|---|---|
             * | 7 | 8 | 9 |
             * |---|---|---|
             */
            matriz[0][0] = dados[linha - 1][coluna - 1]; //posi��o 1
            matriz[0][1] = dados[linha - 1][coluna];     //posi��o 2
            matriz[0][2] = dados[linha - 1][coluna + 1]; //posi��o 3
            matriz[1][0] = dados[linha][coluna - 1];     //posi��o 4
            matriz[1][1] = dados[linha][coluna];         //posi��o 5 Posi��o do pixel que ir� receber o resultado da opera��o
            matriz[1][2] = dados[linha][coluna + 1];     //posi��o 6
            matriz[2][0] = dados[linha + 1][coluna - 1]; //posi��o 7
            matriz[2][1] = dados[linha + 1][coluna];     //posi��o 8
            matriz[2][2] = dados[linha + 1][coluna + 1]; //posi��o 9
                       
            resultado_mascara_x   = ((mascara[0]) * matriz[0][0]) +
                                    ((mascara[1]) * matriz[0][1]) +
                                    ((mascara[2]) * matriz[0][2]) +
                                    ((mascara[0]) * matriz[1][0]) +
                                    ((mascara[1]) * matriz[1][1]) +
                                    ((mascara[2]) * matriz[1][2]) +
                                    ((mascara[0]) * matriz[2][0]) +
                                    ((mascara[1]) * matriz[2][1]) +
                                    ((mascara[2]) * matriz[2][2]);


            resultado_mascara_y   = ((mascara[2]) * matriz[0][0]) +
                                    ((mascara[2]) * matriz[0][1]) +
                                    ((mascara[2]) * matriz[0][2]) +
                                    ((mascara[1]) * matriz[1][0]) +
                                    ((mascara[1]) * matriz[1][1]) +
                                    ((mascara[1]) * matriz[1][2]) +
                                    ((mascara[0]) * matriz[2][0]) +
                                    ((mascara[0]) * matriz[2][1]) +
                                    ((mascara[0]) * matriz[2][2]);

            if(resultado_mascara_x < 0)
                resultado_mascara_x = resultado_mascara_x * (-1);
            if(resultado_mascara_y < 0)
                resultado_mascara_y = resultado_mascara_y * (-1);
            
            if(resultado_mascara_x + resultado_mascara_y > 255) enviarSerial(255);
            else enviarSerial(resultado_mascara_x + resultado_mascara_y);
        }
        putrsUSART("\r\n");
    }
    putrsUSART("\r\n");
}
    
