void configurarUSART(){
	OpenUSART(USART_TX_INT_OFF &  // Transmit Interrupt OFF OBS: Tem no datasheet -> mudei para ON
            USART_RX_INT_ON & // Receive Interrupt ON OBS: Tem no datasheet -> mudei para ON
            USART_ASYNCH_MODE & // Asynchronous Mode OBS: Tem no datasheet
            USART_EIGHT_BIT & // 8-bit Transmit/Receive OBS: Tem no datasheet
            USART_CONT_RX & // Continuous Reception OBS: Pesquisar para qu? serve. Deve ter no datasheet
			  		USART_SYNC_SLAVE &
            USART_BRGH_HIGH, // High Baud Rate
    		  	51); // Baud Rate 9600 OBS: Formula no livro
						baudUSART(BAUD_8_BIT_RATE &
			  		BAUD_AUTO_OFF &
		      	BAUD_WAKEUP_OFF);
	
	BAUDCONbits.RXDTP = 0; //Inversao
	BAUDCONbits.TXCKP = 0; //Inversao
}
   

void send_Serial(int soma)
{
    int i, centena, dezena, unidade, n, aux;
    n = soma;
    
    if(soma > 100)
    {
        centena=n/100;
        centena = centena + 48;
        aux = n%100;
        dezena = aux/10;
        dezena = dezena + 48;
        aux = n%100;
        unidade = aux%10;
        unidade = unidade + 48;
        Delay100TCYx(100);
        putcUSART(centena);
        Delay100TCYx(100);
        putcUSART(dezena);
        Delay100TCYx(100);
        putcUSART(unidade);
        putcUSART(32);
        
    }
    else
    {
        dezena = n/10;
        dezena = dezena + 48;
        aux = n%10;
        unidade = aux + 48;
        Delay100TCYx(100);
        putcUSART(dezena);
        Delay100TCYx(100);
        putcUSART(unidade);
        putcUSART(32);
    }  
}

void operadorPrewitt()
{
    // MASCARAS DE PREWITT
    int mascara_x[3][3] = {{1, 0, -1},{1, 0, -1},{1, 0, -1}};
    int mascara_y[3][3] = {{-1, -1, -1},{0, 0, 0},{1, 1, 1}};
    int resultado_mascara_x = 0;
    int resultado_mascara_y = 0;
    int resultado_final     = 0;
    int linha   = 0;
    int coluna  = 0;
    int i, j;
    // Matriz 3x3 que pegará os pixels ao redor do qual estamos operando
    int matriz[3][3];
    
    for(linha = 0; linha < LINHAS; linha++)
    {
        for(coluna = 0; coluna < COLUNAS; coluna++)
        {
            /*
             * Matriz 3x3 que pegará os pixels ao redor do qual estamos operando
             * |---|---|---|
             * | 1 | 2 | 3 |
             * |---|---|---|
             * | 4 | 5 | 6 |
             * |---|---|---|
             * | 7 | 8 | 9 |
             * |---|---|---|
             */
            matriz[0][0] = data[linha - 1][coluna - 1]; //posição 1
            matriz[0][1] = data[linha - 1][coluna];     //posição 2
            matriz[0][2] = data[linha - 1][coluna + 1]; //posição 3
            matriz[1][0] = data[linha][coluna - 1];     //posição 4
            matriz[1][1] = data[linha][coluna];         //posição 5 Posição do pixel que irá receber o resultado da operação
            matriz[1][2] = data[linha][coluna + 1];     //posição 6
            matriz[2][0] = data[linha + 1][coluna - 1]; //posição 7
            matriz[2][1] = data[linha + 1][coluna];     //posição 8
            matriz[2][2] = data[linha + 1][coluna + 1]; //posição 9
            for(i = 0; i < 3; i++)
            {
                for(j = 0; j < 3; j++)
                {
                    if(matriz[i][j] < 0) matriz[i][j]   = 0;
                    if(matriz[i][j] > 255) matriz[i][j] = 0;
                } 
            }
            
            resultado_mascara_x = 0;
            resultado_mascara_y = 0;
            
            resultado_mascara_x   = ((mascara_x[0][0]) * matriz[0][0]) +
                                    ((mascara_x[0][1]) * matriz[0][1]) +
                                    ((mascara_x[0][2]) * matriz[0][2]) +
                                    ((mascara_x[1][0]) * matriz[1][0]) +
                                    ((mascara_x[1][1]) * matriz[1][1]) +
                                    ((mascara_x[1][2]) * matriz[1][2]) +
                                    ((mascara_x[2][0]) * matriz[2][0]) +
                                    ((mascara_x[2][1]) * matriz[2][1]) +
                                    ((mascara_x[2][2]) * matriz[2][2]);

            resultado_mascara_y =   ((mascara_y[0][0]) * matriz[0][0]) +
                                    ((mascara_y[0][1]) * matriz[0][1]) +
                                    ((mascara_y[0][2]) * matriz[0][2]) +
                                    ((mascara_y[1][0]) * matriz[1][0]) +
                                    ((mascara_y[1][1]) * matriz[1][1]) +
                                    ((mascara_y[1][2]) * matriz[1][2]) +
                                    ((mascara_y[2][0]) * matriz[2][0]) +
                                    ((mascara_y[2][1]) * matriz[2][1]) +
                                    ((mascara_y[2][2]) * matriz[2][2]);

            if(resultado_mascara_x < 0)
                resultado_mascara_x = resultado_mascara_x * (-1);
            if(resultado_mascara_y < 0)
                resultado_mascara_y = resultado_mascara_y * (-1);
            resultado_final  = resultado_mascara_x + resultado_mascara_y;
            send_Serial(resultado_final);
        }
        putrsUSART("\r\n");
    }
    putrsUSART("\r\n");
}
    
