/*
 * Função: enviarSerial( int valor )
 * Parâmetros de entrada: O valor resultante do operador prewitt, que é um número inteiro.
 * O que ela faz: Recebe um número inteiro como entrada, o separa em centena, dezena e unidade e envia pela serial o seu caracter correspondente na tabela ASCII.
 * Retorno: vazio
 */
void enviarSerial(int valor)
{
    //Variáveis usadas para realizar a divisão do valor
    int centena, dezena, unidade, aux;
    if(valor >= 100)
    {
        centena = valor/100;
        centena = centena + 48;
        aux     = valor%100;
        dezena  = aux/10;
        dezena  = dezena + 48;
        aux     = valor%100;
        unidade = aux%10;
        unidade = unidade + 48;
        Delay100TCYx(100);
        putcUSART(centena);
        Delay100TCYx(100);
        putcUSART(dezena);
        Delay100TCYx(100);
        putcUSART(unidade);
        putcUSART(32);    
    }
    else
    {
        dezena  = valor/10;
        dezena  = dezena + 48;
        aux     = valor%10;
        unidade = aux + 48;
        Delay100TCYx(100);
        putcUSART(dezena);
        Delay100TCYx(100);
        putcUSART(unidade);
        putcUSART(32);
    }
    
    return;
}

/*
 * Função: operadorPrewitt()
 * Parâmetros de entrada: nenhum
 * O que ela faz: Acessa a matriz dados, que possui os valores de cada pixel da imagem, pega uma janela/matriz 3x3
 *                e faz uma operação de convolução nesta matriz, com o objetivo de identificar as bordas em uma imagem.
 * Retorno: vazio
 */
void operadorPrewitt()
{
    // Mascaras de Prewitt. A mascara_x é a máscara vertical, enquanto a mascara_y é a horizontal.
    int mascara_x[3][3] = {{1, 0, -1},{1, 0, -1},{1, 0, -1}};
    int mascara_y[3][3] = {{-1, -1, -1},{0, 0, 0},{1, 1, 1}};
    
    // Variáveis que vão armazenar os resultados da aplicação das máscaras.
    int resultado_mascara_x = 0;
    int resultado_mascara_y = 0;
    int resultado_final     = 0;
    unsigned char linha  = 0;
    unsigned char coluna = 0;
    
    for(linha = 1; linha < LINHAS-1; linha++)
    {
        for(coluna = 1; coluna < COLUNAS-1; coluna++)
        {
            /*
             * Matriz 3x3 que pegará os pixels ao redor do qual estamos operando.
             * |---|---|---|
             * | 1 | 2 | 3 |
             * |---|---|---|
             * | 4 | 5 | 6 |
             * |---|---|---|
             * | 7 | 8 | 9 |
             * |---|---|---|
             * Neste caso, o pixel que estamos realizando a operação está no centro, ou seja, na posição 5.
             */      
            // Multiplicação de cada posição da mascara x e y pela posição correspondente na janela/matriz 3x3
            resultado_mascara_x   = ((mascara_x[0][0]) * dados[linha - 1][coluna - 1]) +
                                    ((mascara_x[0][1]) * dados[linha - 1][coluna])     +
                                    ((mascara_x[0][2]) * dados[linha - 1][coluna + 1]) +
                                    ((mascara_x[1][0]) * dados[linha][coluna - 1])     +
                                    ((mascara_x[1][1]) * dados[linha][coluna])         +
                                    ((mascara_x[1][2]) * dados[linha][coluna + 1])     +
                                    ((mascara_x[2][0]) * dados[linha + 1][coluna - 1]) +
                                    ((mascara_x[2][1]) * dados[linha + 1][coluna])     +
                                    ((mascara_x[2][2]) * dados[linha + 1][coluna + 1]);
            
            resultado_mascara_y   = ((mascara_y[0][0]) * dados[linha - 1][coluna - 1]) +
                                    ((mascara_y[0][1]) * dados[linha - 1][coluna])     +
                                    ((mascara_y[0][2]) * dados[linha - 1][coluna + 1]) +
                                    ((mascara_y[1][0]) * dados[linha][coluna - 1])     +
                                    ((mascara_y[1][1]) * dados[linha][coluna])         +
                                    ((mascara_y[1][2]) * dados[linha][coluna + 1])     +
                                    ((mascara_y[2][0]) * dados[linha + 1][coluna - 1]) +
                                    ((mascara_y[2][1]) * dados[linha + 1][coluna])     +
                                    ((mascara_y[2][2]) * dados[linha + 1][coluna + 1]);
            
            // Tira o valor absoluto do resultado das operações da mascara x e y, se necessário.
            if(resultado_mascara_x < 0)
                resultado_mascara_x = resultado_mascara_x * (-1);
            if(resultado_mascara_y < 0)
                resultado_mascara_y = resultado_mascara_y * (-1);
                   
            // Soma os resultados calculados anteriormente e passa como parâmetro para a função enviarSerial, com o intuito de "printar" os valores no terminal.          
            resultado_final  = resultado_mascara_x + resultado_mascara_y;
            enviarSerial(resultado_final);
        }
        putrsUSART("\r\n");
    }
    putrsUSART("\r\n\n");
    return;
}

